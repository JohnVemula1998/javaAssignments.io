public class SimpleIfElse {
	//simple if else condition
	public static void main(String[] args) {
		int x = 12;
		int y = 14;
		int z = 40;
		//if else condition
		if(x>y && x>z) {
			System.out.println(x);
		}else if(y>x && y>z){
			System.out.println(y);
		}else {
			System.out.println(z);
		}
		
	}

}
